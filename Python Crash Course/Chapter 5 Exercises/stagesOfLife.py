age = 65 # Input of the person's age
if age < 2:
	print("Your age is a baby")
elif 2 <= age < 4:
	print("You are a toddler")
elif 4 <= age < 13:
	print("You are a kid")
elif 13 <= age < 18:
	print("You are a teenager")
elif 18 <= age < 65:
	print("You are an adult")
elif 65 <= age:
	print("You are an elder")
# Outputs a printed string from nested conditional statements